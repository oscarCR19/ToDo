    public void Ingresar(){
        try{
            if(this.textFieldUsuario.getText().isEmpty()&&String.valueOf(this.textPass.getPassword()).isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Faltan datos, verifique por favor");
                return;
            }else{
                for(int i =0;i<Usuarios.listaUsuarios.size();i++){
                    if(String.valueOf(Usuarios.listaUsuarios.get(i).getUsuario()).equals(this.textFieldUsuario.getText())&&
                            String.valueOf(Usuarios.listaUsuarios.get(i).getContra()).equals(String.valueOf(this.textPass.getPassword()))){
                        
                    }
                }
            }
            this.setUsuario(this.textFieldUsuario.getText());
            ventanaToDo v=new ventanaToDo(this.getUsuario());
            v.setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

